#include <iostream>
using namespace std;
// =  +=   -=  *=   /=   %= 

int main()
{
    int a = 10;

    int b = 5; 

    a += 5; // a = a + 5
    b -= 4; // b = b - 4
    cout << "a  += 5 =" << a  << endl; // 110

    cout << "b  -= 4 =" << b   << endl; 

    
    // system("pause");
    return 0;
}  